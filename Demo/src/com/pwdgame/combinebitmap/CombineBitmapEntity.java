package com.pwdgame.combinebitmap;

public class CombineBitmapEntity {
	float x;
	float y;
	float width;
	float height;
	static int devide = 1;
	int index = -1;

	@Override
	public String toString() {
		return "MyBitmap [x=" + x + ", y=" + y + ", width=" + width
				+ ", height=" + height + ", devide=" + devide + ", index="
				+ index + "]";
	}
}
